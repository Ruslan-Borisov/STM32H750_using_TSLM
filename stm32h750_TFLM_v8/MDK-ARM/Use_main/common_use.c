/**
 * @brief   Callback завершения кадра DCMI (вызывается из прерывания)
 * @param   hdcmi   Указатель на структуру DCMI_HandleTypeDef
 * 
 * @details Выполняет переключение буферов в Ping-Pong режиме:
 *          1. Переключает активный буфер
 *          2. Обновляет указатель DMA на новый буфер
 *          3. Инвалидирует кэш для готового буфера
 *          4. Обновляет счетчик FPS
 *          5. Устанавливает флаг готовности кадра
 * 
 * @note    Вызывается из прерывания - должна быть быстрой
 * @note    Используется двойная буферизация для непрерывного захвата
 */
void HAL_DCMI_FrameEventCallback(DCMI_HandleTypeDef *hdcmi)
{
    static uint32_t count = 0;
    uint32_t current_tick = HAL_GetTick();
    
    /* Переключение буферов */
    if (active_buffer == 0) {
        active_buffer = 1;
        ready_buffer = 0;
        hdcmi->Instance->DR = (uint32_t)pic[1];
    } else {
        active_buffer = 0;
        ready_buffer = 1;
        hdcmi->Instance->DR = (uint32_t)pic[0];
    }
    
    /* Инвалидация кэша для готового буфера (Cortex-M7 с кэшем) */
    SCB_InvalidateDCache_by_Addr((uint32_t*)pic[ready_buffer], FRAME_BUFFER_SIZE);
    
    /* Подсчет FPS */
    count++;
    if (current_tick - LastFPSUpdate >= 1000) {
        Camera_FPS = count;
        count = 0;
        LastFPSUpdate = current_tick;
    }
    
    /* Обновление флагов и счетчиков */
    irq_counter++;
    DCMI_FrameIsReady = 1;
}

/**
 * @brief   Callback ошибки DCMI
 * @param   hdcmi   Указатель на структуру DCMI_HandleTypeDef
 * 
 * @details При возникновении ошибки DMA:
 *          1. Увеличивает счетчик ошибок
 *          2. Останавливает DCMI
 *          3. Перезапускает DMA с текущим активным буфером
 * 
 * @note    Позволяет восстановить захват после временных сбоев
 */
void HAL_DCMI_ErrorCallback(DCMI_HandleTypeDef *hdcmi)
{
    error_counter++;
    
    /* Перезапуск DMA */
    HAL_DCMI_Stop(hdcmi);
    HAL_Delay(10);
    HAL_DCMI_Start_DMA(hdcmi, DCMI_MODE_CONTINUOUS, 
                       (uint32_t)pic[active_buffer], 
                       FrameWidth * FrameHeight);
}


