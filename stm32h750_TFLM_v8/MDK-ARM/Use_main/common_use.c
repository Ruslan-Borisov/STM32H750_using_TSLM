/**
 * @brief   Рисование пунктирной рамки (для индикации потери цели)
 * @param   x       Координата X верхнего левого угла
 * @param   y       Координата Y верхнего левого угла
 * @param   w       Ширина рамки в пикселях
 * @param   h       Высота рамки в пикселях
 * @param   color   Цвет рамки в формате RGB565
 * 
 * @note    Используется для визуализации потери отслеживания объекта
 * @note    Длина штриха - 2 пикселя, пробела - 2 пикселя
 */
void DrawDashedRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    // Верхняя горизонтальная линия (пунктир)
    for (uint16_t i = 0; i < w; i += 4) {
        uint16_t segment = (w - i > 2) ? 2 : (w - i);
        ST7735_LCD_Driver.DrawHLine(&st7735_pObj, x + i, y, segment, color);
    }
    
    // Нижняя горизонтальная линия (пунктир)
    for (uint16_t i = 0; i < w; i += 4) {
        uint16_t segment = (w - i > 2) ? 2 : (w - i);
        ST7735_LCD_Driver.DrawHLine(&st7735_pObj, x + i, y + h, segment, color);
    }
    
    // Левая вертикальная линия (пунктир)
    for (uint16_t i = 0; i < h; i += 4) {
        uint16_t segment = (h - i > 2) ? 2 : (h - i);
        ST7735_LCD_Driver.DrawVLine(&st7735_pObj, x, y + i, segment, color);
    }
    
    // Правая вертикальная линия (пунктир)
    for (uint16_t i = 0; i < h; i += 4) {
        uint16_t segment = (h - i > 2) ? 2 : (h - i);
        ST7735_LCD_Driver.DrawVLine(&st7735_pObj, x + w, y + i, segment, color);
    }
}

/**
 * @brief   Вывод текста поверх видео с фоном
 * @param   x       Координата X начала текста
 * @param   y       Координата Y начала текста
 * @param   text    Указатель на строку для вывода
 * @param   color   Цвет текста в формате RGB565
 * @param   bg      Цвет фона в формате RGB565 (BLACK для прозрачности)
 * 
 * @note    Использует фиксированную ширину символа 6 пикселей
 * @note    Высота символа фиксирована - 12 пикселей
 */
void LCD_ShowTextOverlay(uint16_t x, uint16_t y, char* text, uint16_t color, uint16_t bg)
{
    if (!text) return;
    
    uint8_t char_width = 6;   /*!< Ширина символа в пикселях */
    uint8_t char_height = 12; /*!< Высота символа в пикселях */
    uint16_t text_width = strlen(text) * char_width;
    uint16_t temp_point = POINT_COLOR;
    uint16_t temp_back = BACK_COLOR;
    
    /* Устанавливаем цвета */
    POINT_COLOR = color;
    BACK_COLOR = bg;
    
    /* Рисуем прямоугольник фона (если bg не прозрачный) */
    if (bg != 0xFFFF) {  /* 0xFFFF = TRANSPARENT */
        ST7735_LCD_Driver.FillRect(&st7735_pObj, x, y, text_width, char_height, bg);
    }
    
    /* Выводим текст с правильными параметрами:
       x, y - координаты
       width = text_width - ширина всей строки
       height = char_height - высота области
       size = char_height - размер шрифта (12)
    */
    LCD_ShowString(x, y, text_width, char_height, char_height, (uint8_t*)text);
    
    /* Восстанавливаем цвета */
    POINT_COLOR = temp_point;
    BACK_COLOR = temp_back;
}
