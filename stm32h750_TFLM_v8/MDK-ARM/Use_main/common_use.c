/**
 * @brief   Обработка нажатия кнопки с антидребезгом
 * @details Переключение режимов работы приложения
 * 
 * @note    Использует BUTTON_DEBOUNCE для подавления дребезга
 * @note    Кнопка подключена с активным низким уровнем (PRESS = 0)
 */
void HandleButton(void)
{
    uint32_t now = HAL_GetTick();
    static uint32_t last_press = 0;
    
    /* Проверка нажатия (активный низкий уровень) */
    if (HAL_GPIO_ReadPin(KEY_GPIO_Port, KEY_Pin) == GPIO_PIN_RESET)
    {
        /* Антидребезг */
        if (now - last_press > BUTTON_DEBOUNCE) {
            last_press = now;
            
            /* Здесь можно добавить переключение режимов */
            // current_mode = (current_mode + 1) % 4;
            
            LED_Blink(50, 50);  /* Визуальное подтверждение */
        }
    }
}

