/**
 * @brief   Управление встроенным светодиодом (мигание)
 * @param   Hdelay  Длительность горения в миллисекундах
 * @param   Ldelay  Длительность паузы в миллисекундах
 * 
 * @note    Блокирующая функция, использует HAL_Delay
 * @note    Светодиод подключен к пину PE3
 */
void LED_Blink(uint32_t Hdelay, uint32_t Ldelay)
{
    HAL_GPIO_WritePin(PE3_GPIO_Port, PE3_Pin, GPIO_PIN_SET);   /* Включить */
    HAL_Delay(Hdelay);
    HAL_GPIO_WritePin(PE3_GPIO_Port, PE3_Pin, GPIO_PIN_RESET); /* Выключить */
    HAL_Delay(Ldelay);
}

