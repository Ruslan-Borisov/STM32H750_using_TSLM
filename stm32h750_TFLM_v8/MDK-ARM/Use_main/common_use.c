/**
 * @brief   Вывод тестового паттерна на дисплей
 * @details Рисует три вертикальные полосы (красный, зеленый, синий)
 *          для проверки цветопередачи и работы дисплея
 * 
 * @note    Паттерн отображается в течение 2 секунд
 * @note    После паттерна экран очищается (черный)
 */
void LCD_TestPattern(void)
{
    uint16_t test_buf[LCD_HEIGHT][LCD_WIDTH];
    
    /* Заполнение буфера цветными полосами */
    for(int y = 0; y < LCD_HEIGHT; y++) {
        for(int x = 0; x < LCD_WIDTH; x++) {
            if (x < LCD_WIDTH/3) {
                test_buf[y][x] = RED;      /* Левая треть - красный */
            } else if (x < 2*LCD_WIDTH/3) {
                test_buf[y][x] = GREEN;    /* Средняя треть - зеленый */
            } else {
                test_buf[y][x] = BLUE;     /* Правая треть - синий */
            }
        }
    }
    
    /* Отображение паттерна */
    ST7735_FillRGBRect(&st7735_pObj, 0, 0, (uint8_t *)test_buf, LCD_WIDTH, LCD_HEIGHT);
    HAL_Delay(2000);
    ST7735_LCD_Driver.FillRect(&st7735_pObj, 0, 0, LCD_WIDTH, LCD_HEIGHT, BLACK);
    HAL_Delay(500);
}

