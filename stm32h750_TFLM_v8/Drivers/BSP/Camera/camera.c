/**
  * @brief   Запись в регистр с 16-битной адресацией (для OV5640)
  * @param   hov        Указатель на структуру камеры
  * @param   reg_addr   16-битный адрес регистра
  * @param   reg_data   Байт данных для записи
  * 
  * @retval  Camera_OK (0) при успехе, camera_ERROR (1) при ошибке I2C
  * 
  * @note    OV5640 использует 16-битные адреса регистров, в отличие от
  *          других камер OmniVision (OV7670, OV2640, OV7725).
  * @note    Используем HAL_I2C_Mem_Write для упрощения кода.
  */
int32_t Camera_WriteRegb2(Camera_HandleTypeDef *hov, uint16_t reg_addr, uint8_t reg_data)
{
    /* Запись в память по 16-битному адресу */
    if (HAL_I2C_Mem_Write(hov->hi2c, hov->addr + 1, reg_addr,
                          I2C_MEMADD_SIZE_16BIT, &reg_data, 1, hov->timeout) == HAL_OK)
    {
        return Camera_OK;
    }
    else
    {
        return camera_ERROR;
    }
}

/**
  * @brief   Чтение из регистра с 16-битной адресацией (для OV5640)
  * @param   hov        Указатель на структуру камеры
  * @param   reg_addr   16-битный адрес регистра
  * @param   reg_data   Указатель для сохранения прочитанного байта
  * 
  * @retval  Camera_OK (0) при успехе, camera_ERROR (1) при ошибке I2C
  */
int32_t Camera_ReadRegb2(Camera_HandleTypeDef *hov, uint16_t reg_addr, uint8_t *reg_data)
{
    /* Чтение из памяти по 16-битному адресу */
    if (HAL_I2C_Mem_Read(hov->hi2c, hov->addr + 1, reg_addr,
                         I2C_MEMADD_SIZE_16BIT, reg_data, 1, hov->timeout) == HAL_OK)
    {
        return Camera_OK;
    }
    else
    {
        return camera_ERROR;
    }
}

