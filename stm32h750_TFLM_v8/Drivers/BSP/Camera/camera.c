/**
  * @brief   Чтение идентификатора камеры (производитель и устройство)
  * @param   hov   Указатель на структуру камеры
  * @retval  0 всегда (результат сохраняется в полях структуры)
  * 
  * @note    Процесс определения камеры:
  *          1. Для OV5640 (16-битная адресация) - чтение по специальным адресам
  *          2. Для остальных камер - чтение регистров 0x1C,0x1D (ID производителя)
  *             и 0x0A,0x0B (ID устройства)
  * @note    ID производителя OmniVision обычно 0x7FA2
  *          ID устройств: OV7670 = 0x7673, OV2640 = 0x2642/0x2643, OV7725 = 0x7721/0x7722
  */
int32_t Camera_read_id(Camera_HandleTypeDef *hov)
{
    uint8_t temp[2];  /**< Временный буфер для двух байт */
    temp[0] = 0x01;   /**< Для переключения банка (если необходимо) */
    
    /* Проверяем, не OV5640 ли это (использует 16-битную адресацию) */
    if (hov->addr != OV5640_ADDRESS)
    {
        /* Для OV7670/OV2640/OV7725: переключаемся на банк 0 */
        Camera_WriteReg(hov, 0xFF, temp);  /**< Регистр 0xFF часто используется для выбора банка */
        
        /* Читаем ID производителя (обычно 0x7FA2) */
        Camera_ReadReg(hov, 0x1C, &temp[0]);  /**< Старший байт */
        Camera_ReadReg(hov, 0x1D, &temp[1]);  /**< Младший байт */
        hov->manuf_id = ((uint16_t)temp[0] << 8) | temp[1];  /**< Собираем 16-бит */
        
        /* Читаем ID устройства */
        Camera_ReadReg(hov, 0x0A, &temp[0]);  /**< Старший байт */
        Camera_ReadReg(hov, 0x0B, &temp[1]);  /**< Младший байт */
    }
    else
    {
        /* Для OV5640: читаем ID напрямую по 16-битным адресам */
        Camera_ReadRegb2(&hcamera, OV5640_CHIP_IDH, &temp[0]);  /**< Старший байт */
        Camera_ReadRegb2(&hcamera, OV5640_CHIP_IDL, &temp[1]);  /**< Младший байт */
        hov->manuf_id = 0;  /**< Для OV5640 не читаем производителя отдельно */
    }
    
    /* Сохраняем ID устройства (оба байта) */
    hov->device_id = ((uint16_t)temp[0] << 8) | temp[1];
    return 0;
}

