/**
  * @brief   Программный сброс камеры
  * @param   hov   Указатель на структуру камеры
  * @retval  None
  * 
  * @note    Устанавливает бит сброса в регистре COM7 (0x12)
  * @note    После сброса камера возвращается к настройкам по умолчанию
  * @note    Необходимо подождать ~100 мс для завершения сброса
  */
void Camera_Reset(Camera_HandleTypeDef *hov)
{
    uint8_t temp;
    
    /* Переключаемся на банк 0 (если необходимо) */
    temp = 0x01;
    Camera_WriteReg(hov, 0xFF, &temp);
    
    /* Устанавливаем бит сброса (бит 7 регистра 0x12) */
    temp = 0x80;  /**< 0x80 = 0b10000000 */
    Camera_WriteReg(hov, 0x12, &temp);
    
    /* Ждем завершения сброса */
    HAL_Delay(100);
}

