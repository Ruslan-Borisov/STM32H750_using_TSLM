/**
  * @brief   Запись одного байта в регистр камеры (8-битная адресация)
  * @param   hov     Указатель на структуру камеры (содержит I2C и адрес)
  * @param   regAddr 8-битный адрес регистра для записи
  * @param   pData   Указатель на байт данных для записи
  * 
  * @retval  Camera_OK (0) при успехе, camera_ERROR (1) при ошибке I2C
  * 
  * @note    Протокол SCCB (OmniVision) совместим с I2C, но требует отправки
  *          двух байт: [адрес регистра] [значение]
  * @note    Для STM32H750 важно проверить скорость I2C (обычно 100-400 кГц)
  *          и правильные pull-up резисторы на линии SCL/SDA.
  */
int32_t Camera_WriteReg(Camera_HandleTypeDef *hov, uint8_t regAddr, const uint8_t *pData)
{
    uint8_t tt[2];                     /**< Буфер для передачи: [адрес][данные] */
    tt[0] = regAddr;                    /**< Первый байт - адрес регистра */
    tt[1] = pData[0];                   /**< Второй байт - значение для записи */
    
    /* Отправляем оба байта одной I2C транзакцией */
    if (HAL_I2C_Master_Transmit(hov->hi2c, hov->addr, tt, 2, hov->timeout) == HAL_OK)
    {
        return Camera_OK;                /**< Успешная запись */
    }
    else
    {
        return camera_ERROR;             /**< Ошибка I2C (нет ответа от камеры) */
    }
}

/**
  * @brief   Чтение одного байта из регистра камеры (8-битная адресация)
  * @param   hov     Указатель на структуру камеры
  * @param   regAddr 8-битный адрес регистра для чтения
  * @param   pData   Указатель для сохранения прочитанного байта
  * 
  * @retval  Camera_OK (0) при успехе, camera_ERROR (1) при ошибке I2C
  * 
  * @note    Процедура чтения SCCB:
  *          1. Отправить адрес регистра (запись с адресом устройства + бит записи)
  *          2. Выполнить рестарт (автоматически в HAL)
  *          3. Прочитать данные (запись с адресом устройства + бит чтения)
  */
int32_t Camera_ReadReg(Camera_HandleTypeDef *hov, uint8_t regAddr, uint8_t *pData)
{
    /* Шаг 1: Отправляем адрес регистра, который хотим прочитать */
    HAL_I2C_Master_Transmit(hov->hi2c, hov->addr + 1, &regAddr, 1, hov->timeout);
    
    /* Шаг 2: Читаем один байт данных (автоматический рестарт в HAL) */
    if (HAL_I2C_Master_Receive(hov->hi2c, hov->addr + 1, pData, 1, hov->timeout) == HAL_OK)
    {
        return Camera_OK;                /**< Успешное чтение */
    }
    else
    {
        return camera_ERROR;             /**< Ошибка I2C */
    }
}

