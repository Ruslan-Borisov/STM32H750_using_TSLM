/**
  * @brief   Настройка источника тактового сигнала XCLK для камеры
  * @param   xclktype   Тип источника (XCLK_TIM - TIM1 PWM, XCLK_MCO - MCO1)
  * @retval  None
  * 
  * @note    КРИТИЧЕСКИ ВАЖНО для STM32H750!
  * @note    Камере OV2640 требуется тактовый сигнал 12 МГц на пине XCLK (PA8).
  *          На STM32H750 есть два способа его получить:
  * 
  *          1. XCLK_TIM: Использовать TIM1_CH1 в режиме PWM для генерации 12 МГц
  *             - Плюсы: Стабильная частота, можно точно настроить
  *             - Минусы: Конфликтует с TIM1_CH2N (используется для подсветки LCD)
  *             - Решение: При использовании этого режима подсветка LCD переключается
  *               на программный ШИМ (LCD_SoftPWMCtrlInit)
  * 
  *          2. XCLK_MCO: Использовать MCO1 (Microcontroller Clock Output) на PA8
  *             - Плюсы: Не конфликтует с TIM1, подсветка LCD работает штатно
  *             - Минусы: Менее стабильная частота, на высоких скоростях могут быть
  *               артефакты изображения (помехи, полосы)
  * 
  * @note    Автор рекомендует для OV2640 использовать XCLK_TIM при высоких
  *          частотах кадров, чтобы избежать артефактов.
  */
void Camera_XCLK_Set(uint8_t xclktype)
{
#if USE_LCD
    #include "lcd.h"  /**< Для управления подсветкой LCD */
#endif

    if (xclktype == XCLK_TIM)  /**< Режим 1: TIM1 PWM для XCLK */
    {
        TIM_OC_InitTypeDef sConfigOC = {0};
        GPIO_InitTypeDef GPIO_InitStruct = {0};

        /* Отключаем предыдущую конфигурацию TIM1 и PA8 */
        HAL_GPIO_DeInit(GPIOA, GPIO_PIN_8);
        HAL_TIM_PWM_DeInit(&htim1);

        /**
         * Настройка TIM1 для генерации 12 МГц на канале 1
         * 
         * Расчет:
         * - Тактовая частота TIM1 обычно 120 МГц (зависит от конфигурации RCC)
         * - Prescaler = 1-1 = 0 (деление на 1) -> счетчик тактируется 120 МГц
         * - Period = 10-1 = 9 (счет до 9) -> период = 10 тактов
         * - Частота PWM = 120 МГц / 10 = 12 МГц
         */
        htim1.Instance = TIM1;
        htim1.Init.Prescaler = 1 - 1;         /**< Делитель = 1 (без деления) */
        htim1.Init.CounterMode = TIM_COUNTERMODE_UP;
        htim1.Init.Period = 10 - 1;            /**< Период = 10 тактов */
        htim1.Init.ClockDivision = TIM_CLOCKDIVISION_DIV1;
        htim1.Init.RepetitionCounter = 0;
        htim1.Init.AutoReloadPreload = TIM_AUTORELOAD_PRELOAD_DISABLE;
        
        if (HAL_TIM_PWM_Init(&htim1) != HAL_OK)
        {
            Error_Handler();
        }

        /* Настройка канала PWM: режим, полярность, коэффициент заполнения */
        sConfigOC.OCMode = TIM_OCMODE_PWM1;
        sConfigOC.Pulse = 5;                    /**< 50% заполнение (5/10) */
        sConfigOC.OCPolarity = TIM_OCPOLARITY_HIGH;
        sConfigOC.OCNPolarity = TIM_OCNPOLARITY_HIGH;
        sConfigOC.OCFastMode = TIM_OCFAST_DISABLE;
        sConfigOC.OCIdleState = TIM_OCIDLESTATE_RESET;
        sConfigOC.OCNIdleState = TIM_OCNIDLESTATE_RESET;
        
        if (HAL_TIM_PWM_ConfigChannel(&htim1, &sConfigOC, TIM_CHANNEL_1) != HAL_OK)
        {
            Error_Handler();
        }

        /* Настройка пина PA8 как TIM1_CH1 (альтернативная функция AF1) */
        __HAL_RCC_GPIOA_CLK_ENABLE();
        GPIO_InitStruct.Pin = GPIO_PIN_8;
        GPIO_InitStruct.Mode = GPIO_MODE_AF_PP;
        GPIO_InitStruct.Pull = GPIO_NOPULL;
        GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_VERY_HIGH;
        GPIO_InitStruct.Alternate = GPIO_AF1_TIM1;  /**< AF1 для TIM1 на H7 */
        HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

        /* Запускаем PWM генерацию */
        HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);

#if USE_LCD
        /* Так как TIM1 занят XCLK, переключаем подсветку LCD на программный ШИМ */
        LCD_SoftPWMCtrlInit();
#endif
    }
    else  /**< Режим 2: XCLK_MCO - используем MCO1 */
    {
#if USE_LCD
        /* Возвращаем подсветку LCD на аппаратный ШИМ (TIM1_CH2N) */
        LCD_SoftPWMCtrlDeInit();
#endif

        /* Отключаем TIM1 с PA8 */
        HAL_GPIO_DeInit(GPIOA, GPIO_PIN_8);
        HAL_TIM_PWM_DeInit(&htim1);

#if USE_LCD
        /* Восстанавливаем стандартную инициализацию TIM1 для подсветки */
        MX_TIM1_Init();
        LCD_SoftPWMEnable(0);
        HAL_TIMEx_PWMN_Start(&htim1, TIM_CHANNEL_2);
#endif

        /**
         * Настройка MCO1 для вывода 12 МГц на PA8
         * 
         * Используем HSI48 (внутренний генератор 48 МГц) как источник
         * Делитель: RCC_MCODIV_4 = 48/4 = 12 МГц
         */
        HAL_RCC_MCOConfig(RCC_MCO1, RCC_MCO1SOURCE_HSI48, RCC_MCODIV_4);
    }
}

