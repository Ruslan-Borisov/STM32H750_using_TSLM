/**
  * @brief   Инициализация камеры с автоматическим определением типа
  * @param   hi2c        Указатель на структуру I2C (какой I2C интерфейс использовать)
  * @param   framesize   Желаемое разрешение (из перечисления framesize_t)
  * @retval  None (результат сохраняется в глобальной структуре hcamera)
  * 
  * @note    Алгоритм определения:
  *          1. Пробуем адрес OV7670, читаем ID
  *          2. Если не OV7670, пробуем OV2640
  *          3. Если не OV2640, пробуем OV7725
  *          4. Если не OV7725, пробуем OV5640
  *          5. Если ни одна не ответила - hcamera.device_id = 0
  * 
  * @note    После успешного определения вызывается соответствующая
  *          функция инициализации (ov2640_init и т.д.), которая применяет
  *          список регистров для нужного разрешения и формата.
  */
void Camera_Init_Device(I2C_HandleTypeDef *hi2c, framesize_t framesize)
{
    /* Сохраняем указатель на I2C в глобальной структуре */
    hcamera.hi2c = hi2c;
    hcamera.addr = OV7670_ADDRESS;  /**< Начинаем с OV7670 (наиболее распространенный) */
    hcamera.timeout = 100;           /**< Таймаут I2C операций 100 мс */

    /* Шаг 1: Пробуем OV7670 */
    Camera_read_id(&hcamera);
    if (hcamera.manuf_id == 0x7fa2 && hcamera.device_id == 0x7673)
    {
        /* Это OV7670! */
        OV7670_Config();  /**< Специфичная инициализация для OV7670 */
    }
    else
    {
        /* Шаг 2: Пробуем OV2640 (ваша камера!) */
        hcamera.addr = OV2640_ADDRESS;
        Camera_read_id(&hcamera);
        if (hcamera.manuf_id == 0x7fa2 && ((hcamera.device_id - 0x2641) <= 2))
        {
            /**
             * Это OV2640! (device_id обычно 0x2642 или 0x2643)
             * 
             * ВНИМАНИЕ: Автор отмечает проблему с XCLK!
             * При использовании высокоскоростных режимов (высокий FPS)
             * XCLK от MCO может давать артефакты изображения.
             * 
             * Раскомментируйте следующую строку, если заметите полосы/шумы:
             * Camera_XCLK_Set(XCLK_TIM);
             */
            
            /* Инициализация OV2640 с указанным разрешением */
            ov2640_init(framesize);
        }
        else
        {
            /* Шаг 3: Пробуем OV7725 */
            hcamera.addr = OV7725_ADDRESS;
            Camera_read_id(&hcamera);
            if (hcamera.manuf_id == 0x7fa2 && ((hcamera.device_id - 0x7721) <= 2))
            {
                /* Это OV7725 (аналогичная проблема с XCLK) */
                // Camera_XCLK_Set(XCLK_TIM);
                ov7725_init(framesize);
            }
            else
            {
                /* Шаг 4: Пробуем OV5640 */
                hcamera.addr = OV5640_ADDRESS;
                Camera_read_id(&hcamera);
                if (hcamera.device_id == 0x5640)
                {
                    /* Это OV5640 (требует 16-битной адресации) */
                    /* Примечание: автофокус не реализован в этом драйвере */
                    ov5640_init(framesize);
                }
                else
                {
                    /* Камера не найдена! Обнуляем адрес и ID */
                    hcamera.addr = 0;
                    hcamera.device_id = 0;
                }
            }
        }
    }
}

