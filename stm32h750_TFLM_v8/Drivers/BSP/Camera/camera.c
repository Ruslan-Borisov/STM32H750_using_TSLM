/**
  * @brief   Запись списка регистров (массива конфигурации)
  * @param   hov        Указатель на структуру камеры
  * @param   reg_list   Указатель на массив структур regval_t
  * 
  * @retval  Camera_OK (0) при успехе, код ошибки при первой неудачной записи
  * 
  * @note    Формат списка: {адрес регистра, значение}
  * @note    Список должен заканчиваться {0xFF, 0xFF} как терминатор
  * @note    Это основной способ конфигурации камеры - все настройки
  *          (разрешение, формат, яркость и т.д.) задаются через такие списки
  */
int32_t Camera_WriteRegList(Camera_HandleTypeDef *hov, const struct regval_t *reg_list)
{
    const struct regval_t *pReg = reg_list;  /**< Указатель на текущий элемент списка */
    
    /* Проходим по списку, пока не встретим терминатор 0xFF,0xFF */
    while (pReg->reg_addr != 0xFF && pReg->value != 0xFF)
    {
        /* Записываем текущий регистр */
        int write_result = Camera_WriteReg(hov, pReg->reg_addr, &(pReg->value));
        if (write_result != Camera_OK)
        {
            return write_result;  /**< При ошибке немедленно возвращаем код ошибки */
        }
        pReg++;  /**< Переходим к следующей записи в списке */
    }
    return Camera_OK;  /**< Все регистры успешно записаны */
}

