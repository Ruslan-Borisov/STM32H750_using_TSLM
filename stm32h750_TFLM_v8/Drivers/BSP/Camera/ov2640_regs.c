/**
  * @brief  Регистры для SVGA режима (800x600 и меньше)
  * 
  * Эти регистры настраивают сенсор и DSP для работы с разрешениями
  * до SVGA (800x600). Используются в _set_framesize() когда ширина <= SVGA.
  */
const uint8_t OV2640_svga_regs[][2] = {
        /* Настройка сенсора для SVGA */
        { BANK_SEL, BANK_SEL_SENSOR },  // Переключаемся на банк сенсора
        
        /* DSP input image resolution and window size control */
        { COM7,    COM7_RES_SVGA},      // Устанавливаем SVGA (800x600) как базовое разрешение
        { COM1,    0x0F },              /* UXGA=0x0F, SVGA=0x0A, CIF=0x06 */
                                         // COM1: для SVGA обычно 0x0A, но здесь 0x0F (возможно другое)
        { REG32,   0x09 },              /* UXGA=0x36, SVGA/CIF=0x09 */
                                         // REG32: настройка размера окна
        
        /* Окно захвата (window) - координаты активной области */
        { HSTART,  0x11 },              /* UXGA=0x11, SVGA/CIF=0x11 */
                                         // Начало по горизонтали
        { HSTOP,   0x43 },              /* UXGA=0x75, SVGA/CIF=0x43 */
                                         // Конец по горизонтали (размер = HSTOP - HSTART)
        
        { VSTART,  0x00 },              /* UXGA=0x01, SVGA/CIF=0x00 */
                                         // Начало по вертикали
        { VSTOP,   0x4b },              /* UXGA=0x97, SVGA/CIF=0x4b */
                                         // Конец по вертикали
        { 0x3d,    0x38 },              /* UXGA=0x34, SVGA/CIF=0x38 */
                                         // Дополнительная настройка размера
        
        /* Аналоговые настройки для SVGA */
        { 0x35,    0xda },              // Настройка усиления
        { 0x22,    0x1a },              // Настройка яркости
        { 0x37,    0xc3 },              // Настройка контраста
        { 0x34,    0xc0 },              // Доп. аналоговые
        { 0x06,    0x88 },              // HSTART/HSTOP (доп.)
        { 0x0d,    0x87 },              // VSTART/VSTOP (доп.)
        { 0x0e,    0x41 },              // Доп.
        { 0x42,    0x03 },              // Доп.
        
        /* Set DSP input image size and offset.
           The sensor output image can be scaled with OUTW/OUTH */
        { BANK_SEL, BANK_SEL_DSP },      // Переключаемся на DSP
        { R_BYPASS, R_BYPASS_DSP_BYPAS }, // Отключаем DSP для настройки
        
        { REG_RESET,   REG_RESET_DVP },  // Сброс DVP интерфейса
        
        /* Установка размера изображения для DSP */
        { HSIZE8,  (SVGA_HSIZE>>3)},     /* Image Horizontal Size HSIZE[10:3] */
                                         // Старшие 8 бит ширины (SVGA_HSIZE/8)
        { VSIZE8,  (SVGA_VSIZE>>3)},     /* Image Vertical Size VSIZE[10:3] */
                                         // Старшие 8 бит высоты (SVGA_VSIZE/8)
        
        /* {HSIZE[11], HSIZE[2:0], VSIZE[2:0]} */
        { SIZEL,   ((SVGA_HSIZE>>6)&0x40) | ((SVGA_HSIZE&0x7)<<3) | (SVGA_VSIZE&0x7)},
                                         // Упаковка младших битов размеров
        
        /* Смещение окна (если нужно) */
        { XOFFL,   0x00 },               /* OFFSET_X[7:0] */
        { YOFFL,   0x00 },               /* OFFSET_Y[7:0] */
        
        /* Размеры для масштабирования (окончательный выходной размер) */
        { HSIZE,   ((SVGA_HSIZE>>2)&0xFF) }, /* H_SIZE[7:0]= HSIZE/4 */
        { VSIZE,   ((SVGA_VSIZE>>2)&0xFF) }, /* V_SIZE[7:0]= VSIZE/4 */
        
        /* V_SIZE[8]/OFFSET_Y[10:8]/H_SIZE[8]/OFFSET_X[10:8] */
        { VHYX,    ((SVGA_VSIZE>>3)&0x80) | ((SVGA_HSIZE>>7)&0x08) },
                                         // Старшие биты размеров и смещений
        
        { TEST,    (SVGA_HSIZE>>4)&0x80}, /* H_SIZE[9] */
                                         // Еще один старший бит ширины
        
        /* Настройки DSP для обработки */
        { CTRL2,   CTRL2_DCW_EN | CTRL2_SDE_EN |
          CTRL2_UV_AVG_EN | CTRL2_CMX_EN | CTRL2_UV_ADJ_EN },
                                         // Включаем: DCW (Downsize), SDE (Special Digital Effect)
                                         // UV усреднение, цветокоррекцию
        
        /* H_DIVIDER/V_DIVIDER - делители для масштабирования */
        { CTRLI,   CTRLI_LP_DP | 0x00},  // Low power mode
        
        /* DVP prescalar - предделитель для DVP интерфейса */
        { R_DVP_SP, R_DVP_SP_AUTO_MODE}, // Автоматический режим
        
        /* Включаем DSP обратно */
        { R_BYPASS, R_BYPASS_DSP_EN },
        { REG_RESET,    0x00 },
        {0, 0},                          // Терминатор
};

